						 retract(memoria(X, YY, _)), assert(memoria(X, YY, livre)),
         	     	     retract(posicao(_, _, _)), assert(posicao(X, YY, P)),!.
		 
mover_para_frente :- posicao(X, Y, P), P = south,  Y < 33, YY is Y + 1, 
						 retract(memoria(X, YY, _)), assert(memoria(X, YY, livre)),
         	     	     retract(posicao(_, _, _)), assert(posicao(X, YY, P)),!.

mover_para_frente :- posicao(X, Y, P), P = east,  X < 58, XX is X + 1, 
						 retract(memoria(XX, Y, _)), assert(memoria(XX, Y, livre)),
        	     	     retract(posicao(_, _, _)), assert(posicao(XX, Y, P)),!.

mover_para_frente :- posicao(X, Y, P), P = west,  X > 0, XX is X - 1, 
						 retract(memoria(XX, Y, _)), assert(memoria(XX, Y, livre)),
         	     	     retract(posicao(_, _, _)), assert(posicao(XX, Y, P)),!.
		     	     
